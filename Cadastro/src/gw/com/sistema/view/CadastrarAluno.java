    private void btnPesquisarListaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarListaActionPerformed
        // TODO add your handling code here:
        
        if (txtPesquisar.getText().isEmpty() && txtCodigo1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Digite um nome ou um código!");
            return;
        }

        String nome = "%" + txtPesquisar.getText() + "%";

        int codigo = 0;
        if (!txtCodigo1.getText().isEmpty()) {
            codigo = Integer.parseInt(txtCodigo1.getText());
        }

        AlunoDAO dao = new AlunoDAO();
        List<Aluno> lista = dao.filtrar(nome, codigo);

        DefaultTableModel model = (DefaultTableModel) tabela_Alunos.getModel();
        model.setNumRows(0);

        for (Aluno al : lista) {
            model.addRow(new Object[] {
                al.getId(),
                al.getNome(),
                al.getCodigo(),
                al.getEmail(),
                al.getCurso(),
                al.getNivel(),
                al.getContacto(),
            });
        }
    }//GEN-LAST:event_btnPesquisarListaActionPerformed

