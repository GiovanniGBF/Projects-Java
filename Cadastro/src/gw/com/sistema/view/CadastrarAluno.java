    public void listar() {
        AlunoDAO dao = new AlunoDAO();
        List<Aluno> lista = dao.listar();
        DefaultTableModel model = (DefaultTableModel) tabela_Alunos.getModel();
        model.setNumRows(0);
        for(Aluno al : lista) {
            model.addRow(new Object[] {
                al.getId(),
                al.getNome(),
                al.getCodigo(),
                al.getEmail(),
                al.getCurso(),
                al.getNivel(),
                al.getContacto(),
            });
        }
    }
    
