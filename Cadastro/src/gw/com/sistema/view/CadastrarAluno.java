    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        
        if (txtNome.getText().isEmpty() ||
            txtCodigo.getText().isEmpty() ||
            txtEmail.getText().isEmpty() ||
            txtCurso.getText().isEmpty() ||
            txtContacto.getText().isEmpty()) {

            JOptionPane.showMessageDialog(null, "PREENCHA TODOS OS CAMPOS!");
            return;
        }

        // Desativa botão e mostra loading
        btnSalvar.setEnabled(false);
        progressBar.setVisible(true);

        // Cria o objeto Aluno
        Aluno obj = new Aluno();
        obj.setNome(txtNome.getText());
        obj.setCodigo(Integer.parseInt(txtCodigo.getText()));
        obj.setEmail(txtEmail.getText());
        obj.setCurso(txtCurso.getText());
        obj.setNivel((String) cbNivel.getSelectedItem());
        obj.setContacto(txtContacto.getText());

        // SwingWorker
        new SwingWorker<Boolean, Void>() {

            @Override
            protected Boolean doInBackground() {
                try {
                    AlunoDAO dao = new AlunoDAO();
                    dao.Salvar(obj); // acesso ao banco
                    return true;
                } catch (Exception e) {
                    e.printStackTrace();
                    return false;
                }
            }

            @Override
            protected void done() {
                progressBar.setVisible(false);
                btnSalvar.setEnabled(true);

                try {
                    if (get()) {
                        JOptionPane.showMessageDialog(null, "Aluno salvo com sucesso!");
                    } else {
                        JOptionPane.showMessageDialog(null, "Erro ao salvar aluno!");
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Erro inesperado!");
                }
            }

        }.execute();
    }//GEN-LAST:event_btnSalvarActionPerformed

